line = 0
while line < 10 :
    num4 = 1
    while num4 < 11 :
        print(f"{line*10 + num4}", end = " ")
        if num4%10 == 0 :
            print("\n")                                             # 이렇게 하니깐 한 줄이 빔.
        num4 += 1
    line += 1

